    private void drawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drawButtonActionPerformed
        // TODO add your handling code here:
        String X;
        String Y;
        String L;
        String W;
        X = positionX.getText();
        Y = positionY.getText();
        L = length.getText();
        W = width.getText();
        
        if(!isInteger(X)){
            positionX.setText("invalid");
        }
        if(!isInteger(Y)){
            positionY.setText("invalid");
        }
        if(!isInteger(L)){
            length.setText("invalid");
        }
        if(!isInteger(W)){
            width.setText("invalid");
        }
        
        if(!isInteger(X)||!isInteger(Y)||!isInteger(L)||!isInteger(W)){
//            JOptionPane.showMessageDialog(null,"Invalid input!");
            return;
        }
        Point p = new Point(Integer.parseInt(X),Integer.parseInt(Y));
        
//The following checks if the entered value is negative
//        if(p.x<0||p.y<0||Integer.parseInt(L)<0||Integer.parseInt(W)<0){
//            JOptionPane.showMessageDialog(null,"Invalid input, cannot enter a negative number!");
//            return;
//        }
        if(L.equals(W))
            JOptionPane.showMessageDialog(null,"You entered dimensions of a square!");
        Rectangle r = new Rectangle(p,Integer.parseInt(L),Integer.parseInt(W));
        r.setColor(Color.BLACK);
        canvas.addShape(r);
        canvas.refresh(g);
        int tr = canvas.rectangleCounter++;
        tr++;
        comboBox.addItem("Rectangle "+ tr);
        j.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_drawButtonActionPerformed

